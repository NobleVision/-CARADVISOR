// Bundles the Vercel serverless functions with esbuild so they are fully
// self-contained ESM. This resolves every relative/`server/*` import (and the
// `server/routers.ts` vs `server/routers/` directory collision) at build time,
// because Node's production ESM loader rejects directory and extensionless
// imports (ERR_UNSUPPORTED_DIR_IMPORT). node_modules stay external and are
// traced/included by Vercel. The local Express dev server still imports the
// TypeScript directly (tsx resolves it), so this only affects the prod build.
import { build } from "esbuild";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const common = {
  bundle: true,
  platform: "node",
  format: "esm",
  target: "node20",
  // Keep dependencies external; Vercel's file tracing includes them. Only our
  // own source (including `@shared/*`) is inlined.
  packages: "external",
  alias: {
    "@shared": path.join(root, "shared"),
    "@": path.join(root, "client", "src"),
  },
  logLevel: "info",
  banner: { js: "// Generated by scripts/build-functions.mjs — do not edit." },
};

const targets = [
  { entry: "server/_serverless/trpc.ts", out: "api/trpc/[trpc].js" },
  { entry: "server/_serverless/cron.ts", out: "api/cron/monitor.js" },
];

for (const t of targets) {
  await build({
    ...common,
    entryPoints: [path.join(root, t.entry)],
    outfile: path.join(root, t.out),
  });
  console.log(`✓ bundled ${t.out}`);
}
